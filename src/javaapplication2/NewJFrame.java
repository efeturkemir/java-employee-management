    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
         DefaultTableModel obj = (DefaultTableModel) this.jTable1.getModel();
    TableRowSorter<DefaultTableModel> obj1 = new TableRowSorter<>(obj);
    this.jTable1.setRowSorter(obj1);
    
    obj1.setRowFilter(new RowFilter<DefaultTableModel, Integer>() {
        @Override
        public boolean include(Entry<? extends DefaultTableModel, ? extends Integer> entry) {
            String searchText = jTextField4.getText().toLowerCase(); // Convert search text to lowercase
            int columnCount = entry.getModel().getColumnCount();
            
            // Iterate through the columns and check if any cell contains the search text
            for (int i = 0; i < columnCount; i++) {
                Object cellValue = entry.getModel().getValueAt(entry.getIdentifier(), i);
                if (cellValue != null) {
                    String cellText = cellValue.toString().toLowerCase(); // Convert cell text to lowercase
                    if (cellText.contains(searchText)) {
                        return true; // Include the row if a match is found
                    }
                }
            }
            return false; // Exclude the row if no match is found
        }
    });// TODO add your handling code here:
    }//GEN-LAST:event_jTextField4KeyReleased

